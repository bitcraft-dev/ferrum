/// Emit an expression as a Rust expression string.
/// Called recursively; does not write to the IndentWriter directly.
fn emit_expr(expr: &Expr) -> String {
    match &expr.kind {

        ExprKind::Literal(lit) => emit_literal(lit),

        ExprKind::Ident(ident) => {
            // Program-level variables are static mut — wrap in unsafe read
            // Block-scoped variables are let bindings — plain name
            // The distinction is tracked by whether the ident is uppercase
            // in the emitted names (program-level = UPPERCASE_SNAKE).
            // For simplicity emit as lowercase; the caller knows context.
            to_snake(ident)
        }

        ExprKind::Read(device) => {
            let inst = to_snake(device);
            // INPUT → is_high() returns bool (HIGH=true, LOW=false)
            // ANALOG_INPUT → read() returns u16
            // Type is filled by semantic pass; use it to decide
            match &expr.ty {
                Some(Type::PinState)  => format!("{}.output.is_high().unwrap()", inst),
                Some(Type::AnalogRaw) => format!("{}.output.read()", inst),
                _                     => format!("{}.output.is_high().unwrap()", inst),
            }
        }

        ExprKind::ReadPercent(device) => {
            let inst = to_snake(device);
            // READ_PERCENT → read raw, scale to 0.0–100.0
            format!("({}.output.read() as f32 / 1023.0 * 100.0)", inst)
        }

        ExprKind::BinOp { op, left, right } => {
            let l = emit_expr(left);
            let r = emit_expr(right);
            match op {
                BinOp::Add   => format!("({} + {})",  l, r),
                BinOp::Sub   => format!("({} - {})",  l, r),
                BinOp::Mul   => format!("({} * {})",  l, r),
                BinOp::Div   => format!("({} / {})",  l, r),
                BinOp::Gt    => format!("({} > {})",  l, r),
                BinOp::Lt    => format!("({} < {})",  l, r),
                BinOp::Gte   => format!("({} >= {})", l, r),
                BinOp::Lte   => format!("({} <= {})", l, r),
                BinOp::Eq    => format!("({} == {})", l, r),
                BinOp::NotEq => format!("({} != {})", l, r),
                BinOp::And   => format!("({} && {})", l, r),
                BinOp::Or    => format!("({} || {})", l, r),
            }
        }

        ExprKind::UnaryOp { op, operand } => {
            let o = emit_expr(operand);
            match op {
                UnaryOp::Not => format!("(!{})", o),
                UnaryOp::Neg => format!("(-{})", o),
            }
        }

        ExprKind::Is { target, negated, state } => {
            // mode_btn IS LOW    → mode_btn.output.is_low().unwrap()
            // auto_mode IS TRUE  → auto_mode == true   → auto_mode
            // IS NOT             → negated form
            let target_str = if let ExprKind::Ident(i) = &target.kind {
                to_snake(i)
            } else {
                emit_expr(target)
            };

            let state_str = emit_expr(state);

            // If checking a pin state (HIGH/LOW), use HAL method
            let base = match &state.kind {
                ExprKind::Literal(Literal::High) =>
                    format!("{}.output.is_high().unwrap()", target_str),
                ExprKind::Literal(Literal::Low) =>
                    format!("{}.output.is_low().unwrap()", target_str),
                ExprKind::Literal(Literal::Bool(b)) => {
                    if *b { target_str.clone() }
                    else  { format!("(!{})", target_str) }
                }
                _ => format!("({} == {})", target_str, state_str),
            };

            if *negated { format!("(!{})", base) } else { base }
        }

        ExprKind::Call(call) => {
            let fn_name = to_snake(&call.function);
            let args: Vec<String> = call.args.iter().map(|arg| match &arg.kind {
                CallArgKind::Data(e) => emit_expr(e),
                CallArgKind::Device { ownership, name } => {
                    rust_device_arg(&to_snake(name), ownership)
                }
            }).collect();
            format!("{}({})", fn_name, args.join(", "))
        }

        ExprKind::IfExpr { condition, then_value, else_value } => {
            format!(
                "if {} {{ {} }} else {{ {} }}",
                emit_expr(condition),
                emit_expr(then_value),
                emit_expr(else_value)
            )
        }
    }
}

fn emit_literal(lit: &Literal) -> String {
    match lit {
        Literal::Int(n)     => n.to_string(),
        Literal::Hex(n)     => format!("0x{:X}_u8", n),
        Literal::Decimal(f) => format!("{}_f32", f),
        Literal::Str(s)     => format!("\"{}\"", s),
        Literal::Bool(b)    => if *b { "true".into() } else { "false".into() },
        Literal::High       => "true".into(),
        Literal::Low        => "false".into(),
    }
}

